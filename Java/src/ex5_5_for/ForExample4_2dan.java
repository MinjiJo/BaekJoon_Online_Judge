package ex5_5_for;

public class ForExample4_2dan {
	public static void main(String[] args) {
		int dan=2, i;
		System.out.println("[" + dan + "´Ü]");
		for(i=1; i<10; i++)
			System.out.println(dan+"*"+i+"="+(dan*i));
		
		dan=3;
		System.out.println("[" + dan + "´Ü]");
		for(i=1; i<10; i++)
			System.out.println(dan+"*"+i+"="+(dan*i));
		
		dan=4;
		System.out.println("[" + dan + "´Ü]");
		for(i=1; i<10; i++)
			System.out.println(dan+"*"+i+"="+(dan*i));
	}
}
