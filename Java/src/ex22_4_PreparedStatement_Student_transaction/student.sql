drop table student;

CREATE TABLE STUDENT(
	NO		NUMBER
		CONSTRAINT STUDENT_NO_PK PRIMARY KEY,
	NAME	VARCHAR2(21),
	KOR		NUMBER(3)
		CONSTRAINT STUDENT_KOR_CK
		CHECK(KOR BETWEEN 0 AND 100),
	MATH	NUMBER(3)
		CONSTRAINT STUDENT_MATH_CK
		CHECK(MATH BETWEEN 0 AND 100),
	ENG		NUMBER(3)
		CONSTRAINT STUDENT_ENG_CK
		CHECK(ENG BETWEEN 0 AND 100),
	TOT		NUMBER(3),
	AVG		NUMBER(5,2),
	GRADE	VARCHAR2(3)
		CONSTRAINT STUDENT_GRADE_FK REFERENCES HAKJUM(GRADE)
);

SELECT * FROM STUDENT;