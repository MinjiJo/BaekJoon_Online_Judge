package ex6_2_method_intro;

public class MethodExample1 {
	public static void main(String[] args) {
		for (int i = 1; i<=30; i++) {
			System.out.print("*");
		}System.out.println();
		
		for (int i = 1; i<=30; i++) {
			System.out.print("-");
		}System.out.println();
		
		System.out.println("Hello, Java");
		
		for (int i = 1; i<=30; i++) {
			System.out.print("-");
		}System.out.println();
		
		for (int i = 1; i<=30; i++) {
			System.out.print("*");
		}System.out.println();
	}
}
